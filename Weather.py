Weather = int(input("What is the weather today? (In F)"))
if Weather >= 60 :
    print ("You may wear light clothing if you want to!")
if 50 <= Weather <= 59 :
    print ("You must wear a jacket/sweatshirt!")
if 5 <= Weather <= 49 :
    print ("You must wear a coat!")
if Weather <= 4 :
    print ("DO NOT GO OUT!")

