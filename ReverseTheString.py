NormalString = str(input(" Please enter a, 7 OR LESS! DIGIT! WORD! "))
while len(NormalString) > 7 :
    NormalString = str(input(" Please enter a, 7 OR LESS! DIGIT! WORD! "))
print(" Thank you for entering a, WORD! That is only, 7 OR LESS DIGITS! ")
class Reverse : 
    def __init__(Self) :
        Self.String = "S"
    def ReverseTheString(Self, S) :
        return S[::-1]
ReversedString = Reverse()
ReversedString = ReversedString.ReverseTheString(NormalString)
print(ReversedString)